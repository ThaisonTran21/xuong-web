    public NhanVienView() throws SQLException {
        initComponents();
        setLocationRelativeTo(null);
        model = (DefaultTableModel) tblNhanVien.getModel();
        fillToTable();
        if (tblNhanVien.getRowCount() > 0) {
            index = 0;
            showDeTail();
        }
    }

    private void fillToTable() throws SQLException {
        lstNhanVien = nvs.getAllNhanVien();
        model.setRowCount(0);
        for (NhanVien nv : lstNhanVien) {
            model.addRow(new Object[]{
                nv.getMa(),
                nv.getTenNV(),
                nv.getSDT(),
                nv.isTrangThai()
            });
        }
    }

    private void showDeTail() {
        NhanVien nv = lstNhanVien.get(index);
        txtMa.setText(nv.getMa());
        txtTenNV.setText(nv.getTenNV());
        txtSDT.setText(nv.getSDT());
        cboTrangThai.setSelectedItem(nv.isTrangThai());
        tblNhanVien.setRowSelectionInterval(index, index);
    }

    private NhanVien getNhanVienFromInput() {
        NhanVien nv = new NhanVien();
        nv.setMa(txtMa.getText());
        nv.setTenNV(txtTenNV.getText());
        nv.setSDT(txtSDT.getText());
        if (cboTrangThai.getSelectedIndex() == 0) {
            nv.setTrangThai(true);
        }
        if (cboTrangThai.getSelectedIndex() == 1) {
            nv.setTrangThai(false);
        }
        return nv;
    }

