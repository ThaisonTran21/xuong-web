    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không");
        if (hoi != JOptionPane.YES_OPTION) {
            return;
        }
        String Ma = txtMa.getText();
        try {
            if (nvs.deleteNhanVien(Ma) != null) {
                JOptionPane.showMessageDialog(this, "Xóa Thành Công");
            }else{
                JOptionPane.showMessageDialog(this, "Xóa Thất Bại");
            }
            fillToTable();
        } catch (SQLException ex) {
            Logger.getLogger(NhanVienView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (txtMa.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Mã không bỏ trống");
            txtMa.requestFocus();
            return;
        }
        if (txtTenNV.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Tên không bỏ trống");
            txtTenNV.requestFocus();
            return;
        }
        if (txtSDT.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không bỏ trống");
            txtSDT.requestFocus();
            return;
        }
        
        NhanVien nv = getNhanVienFromInput();
        try {
            if (nvs.addNhanVien(nv) != null) {
                JOptionPane.showMessageDialog(this, "thêm thành công");
            }else{
                JOptionPane.showMessageDialog(this, "thêm thất bại");
            }
            
            fillToTable();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnThemActionPerformed

